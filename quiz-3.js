function greaterOrLower(a, b) {
    if (a > b) {
        console.log("The first number is greater than the second.");
         
    } else if (a < b) {
        console.log("The first number is less than the second.");
        
    } else {
        console.log("The first number is equal to the second.");
    }
    
}
greaterOrLower (2, 15) 